    private void boton_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_entrarActionPerformed
        String usuario = texto_usuario.getText();
        String clave = texto_clave.getText();
        if(usuario.equals("Elcrack2303") && clave.equals("elcrack2303")){
            SistemaPrincipalDiretor ventana = new SistemaPrincipalDiretor();
            ventana.setVisible(true);
            dispose();
        } else if(usuario.equals("CAPpsi") && clave.equals("psicologo")){
            SistemaPrincipalPsicologos ventana = new SistemaPrincipalPsicologos();
            ventana.setVisible(true);
            dispose();
        } else if(usuario.equals("CAPcoor") && clave.equals("coordinador")){
            SistemaPrincipalCoordinador ventana = new SistemaPrincipalCoordinador();
            ventana.setVisible(true);
            dispose();
        } else if(usuario.equals("CAPsec") && clave.equals("secretaria")){
            SistemaPrincipalSecretaria ventana = new SistemaPrincipalSecretaria();
            ventana.setVisible(true);
            dispose();
        } else if(usuario.equals("PSIdir") && clave.equals("director")){
            SistemaPrincipalDiretor ventana = new SistemaPrincipalDiretor();
            ventana.setVisible(true);
            dispose();
        } else{
            JOptionPane.showMessageDialog(null, "Usuario y/o contrase√±a incorrectos.");
        }
    }//GEN-LAST:event_boton_entrarActionPerformed

