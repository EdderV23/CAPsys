    private void boton_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_registrarActionPerformed
        java.util.Date date = fecha_cita.getDate();
        long d = date.getTime();
        java.sql.Date date2 = new java.sql.Date(d);
        String fecha = date2.toString();   
        String nombre = nombre_paciente.getText();
        String cedulaE = cedula_especialista.getText();
        if(nombre.equals("") || cedulaE.equals("")){
            JOptionPane.showMessageDialog(null, "Debe seleccionar paciente y/o especialista.");
        } else{
            try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/centro_atencion_psicologica", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into registro_citas_niños values(?,?,?,?,?,?,?,?,?)");
            
            pst.setString(1, "0");
            pst.setString(2, nombre_paciente.getText().trim());
            pst.setString(3, apellido_paciente.getText().trim());
            pst.setString(4, cedula_paciente.getText().trim());
            pst.setString(5, sexo_paciente.getText().trim());
            pst.setString(6, nombre_especialista.getText().trim());
            pst.setString(7, apellido_especialista.getText().trim());
            pst.setString(8, cedula_especialista.getText().trim());
            pst.setString(9, fecha.trim());
            pst.executeUpdate();
            
            etiquetazo.setText("Registro Exitoso");
        } catch(Exception e){
            
        }
        }
    }//GEN-LAST:event_boton_registrarActionPerformed

