    private void combo_pacientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_pacientesActionPerformed
        try {
            String item = (String) combo_pacientes.getSelectedItem();
            String cedula = item.substring(0,5);
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/centro_atencion_psicologica","root","");
            PreparedStatement pst = cn.prepareStatement("Select * from historia_clinica_niños where Indice = ?");
            
            pst.setString(1, cedula.trim());
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                nombre_paciente.setText(rs.getString("Nombres"));
                apellido_paciente.setText(rs.getString("Apellidos"));
                cedula_paciente.setText(rs.getString("Indice"));
                sexo_paciente.setText(rs.getString("Sexo"));
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_combo_pacientesActionPerformed

