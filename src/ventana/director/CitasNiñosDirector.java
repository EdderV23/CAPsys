public static ArrayList<String> llenarPacientes(){
    ArrayList<String> lista = new ArrayList<String>();
    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/centro_atencion_psicologica","root","");
        PreparedStatement pst = cn.prepareStatement("Select * from historia_clinica_niños");
        
        ResultSet rs = pst.executeQuery();
        
        while(rs.next()){
            lista.add(rs.getString("Indice") + "    " + rs.getString("Nombres"));
        }
    } catch (Exception e) {
    }
    
    return lista;
}
public static ArrayList<String> llenarEspe(){
    ArrayList<String> lista = new ArrayList<String>();
    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/centro_atencion_psicologica","root","");
        PreparedStatement pst = cn.prepareStatement("Select * from especialistas");
        
        ResultSet rs = pst.executeQuery();
        
        while(rs.next()){
            lista.add(rs.getString("Cedula") + " " + rs.getString("Nombres"));
        }
    } catch (Exception e) {
    }
    
    return lista;
}
